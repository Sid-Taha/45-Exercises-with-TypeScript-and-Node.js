// Function for city_country
function city_country(city: string, country: string): string {
  return `${city}, ${country}`;
}

// Result
console.log(city_country("Lahore", "Pakistan"));
console.log(city_country("Tokyo", "Japan"));
console.log(city_country("Paris", "France"));
